package Examen2;

class Person {
	
	/***
	 * Creant la variable.
	 */
	
	private String name;
	
	/***
	 * 
	 * @param name
	 */
	
	/***
	 * 
	 * Creant el constructor. 	 
	 */
	public Person(String name){
		this.name = name;
	}
	
	/***
	 * 
	 * @return
	 */

	/***
	 * 
	 * Creant el getter i el setter del name.
	 */
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
